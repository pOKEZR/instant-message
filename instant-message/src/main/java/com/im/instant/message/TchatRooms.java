package com.im.instant.message;

public class TchatRooms {

	private int idTchatRoom;
	
	private String roomName;

	/**
	 * @return the idTchatRoom
	 */
	public int getIdTchatRoom() {
		return idTchatRoom;
	}

	/**
	 * @param idTchatRoom the idTchatRoom to set
	 */
	public void setIdTchatRoom(int idTchatRoom) {
		this.idTchatRoom = idTchatRoom;
	}

	/**
	 * @return the roomName
	 */
	public String getRoomName() {
		return roomName;
	}

	/**
	 * @param roomName the roomName to set
	 */
	public void setRoomName(String roomName) {
		this.roomName = roomName;
	}
	
	
}
